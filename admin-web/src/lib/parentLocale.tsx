export const localeOptions: Array<{ value: ParentLocale; label: string }> = [
  { value: 'ru', label: 'RU' },
  { value: 'en', label: 'EN' },
  { value: 'kk', label: 'KZ' },
];

export const getParentLocale = (): ParentLocale => {
  if (typeof window === 'undefined') return 'ru';
  const raw = window.localStorage.getItem(PARENT_LOCALE_KEY);
  return raw === 'en' || raw === 'kk' || raw === 'ru' ? raw : 'ru';
};

export const setParentLocale = (locale: ParentLocale) => {
  if (typeof window === 'undefined') return;
  window.localStorage.setItem(PARENT_LOCALE_KEY, locale);
  window.dispatchEvent(new CustomEvent(LOCALE_EVENT, { detail: locale }));
};

export const translateParent = (locale: ParentLocale, key: string) =>
  MESSAGES[locale][key] || MESSAGES.ru[key] || key;

export function useParentLocale() {
  const [locale, setLocaleState] = useState<ParentLocale>('ru');

  useEffect(() => {
    const apply = () => setLocaleState(getParentLocale());
    apply();
    const onChange = () => apply();
    const onStorage = (event: StorageEvent) => {
      if (event.key === PARENT_LOCALE_KEY) apply();
    };
    window.addEventListener(LOCALE_EVENT, onChange as EventListener);
    window.addEventListener('storage', onStorage);
    return () => {
      window.removeEventListener(LOCALE_EVENT, onChange as EventListener);
      window.removeEventListener('storage', onStorage);
    };
  }, []);

  const t = useMemo(() => (key: string) => translateParent(locale, key), [locale]);
  const changeLocale = (next: ParentLocale) => {
    setLocaleState(next);
    setParentLocale(next);
  };

  return { locale, setLocale: changeLocale, t };
}
